class TranslationSystem {
    constructor() {
        this.currentLanguage = localStorage.getItem('gameLanguage') || 'es';
        this.translations = translations;
    }
    
    // Cambiar idioma
    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLanguage = lang;
            localStorage.setItem('gameLanguage', lang);
            this.updatePageTranslations();
            return true;
        }
        return false;
    }
    
    // Obtener traducciÃ³n
    t(key) {
        return this.translations[this.currentLanguage][key] || key;
    }
    
    // Actualizar todas las traducciones de la pÃ¡gina
    updatePageTranslations() {
        // Actualizar elementos con data-translate
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.t(key);
            const translateAttr = element.getAttribute('data-translate-attr');
            
            // Si hay un atributo especÃ­fico para traducir
            if (translateAttr) {
                element.setAttribute(translateAttr, translation);
            }
            // Actualizar segÃºn el tipo de elemento
            else if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                if (element.hasAttribute('placeholder')) {
                    element.placeholder = translation;
                } else {
                    element.value = translation;
                }
            } else if (element.tagName === 'OPTION') {
                element.textContent = translation;
            } else {
                element.textContent = translation;
            }
        });
        
        console.log(`ğŸŒ Idioma cambiado a: ${this.currentLanguage}`);
    }
}

